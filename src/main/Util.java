public class Util {
    public static void printList(float[]list){
        for(int i=0;i<list.length;i++){System.out.print(list[i]+",");}
        System.out.println("");
    }
    public static void printList(int[]list){
        for(int i=0;i<list.length;i++){System.out.print(list[i]+",");}
        System.out.println("");
    }
}
